void main(List<String> args) {
  int age = 30;
  print(--age); //unary //decrement 1
  print(!true); //negation
  print(++age); //increment 1
  print(~1); //unary bitwise complement prefix operator
  print(-age); //flip to negative for display, wont be mutated.
  print(age);
}
//4 types of operators - unary prefix, unary postfix, binary infix, compound assignment.