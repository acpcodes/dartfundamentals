void main(List<String> args) {
  const age1 = 64;
  const age2 = 30;
  const num age = 330.2;
  const result = age + age2 + age1;
  print(result);
  print(result + 30); //compounded operation
  print(age1 - age2); //subtraction
  print(age1 * age2); //multiplication
  print(age1 / age2); // division
  print(age1 ~/ age2); //truncated division
}
